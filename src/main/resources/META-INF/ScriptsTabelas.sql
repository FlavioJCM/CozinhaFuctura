--------------------------------------------------------
--  DDL for Table CHEF
--------------------------------------------------------
CREATE TABLE CHEF(
CPF VARCHAR(14),
NOME VARCHAR(50) NOT NULL,
SENHA VARCHAR(20) NOT NULL,
PRIMARY KEY(CPF)
);

--------------------------------------------------------
--  DDL for Table RECEITA
--------------------------------------------------------

 CREATE TABLE RECEITA (
   CODIGO INT NOT NULL,
   NOME VARCHAR(150) NOT NULL,
   MODO_PREPARO VARCHAR(1000) NOT NULL,
   PRECO FLOAT NOT NULL,
   CPF_CHEF VARCHAR(14) NOT NULL,
   PRIMARY KEY(CODIGO)
);
    
  ALTER TABLE RECEITA 
  ADD FOREIGN KEY (CPF_CHEF) 
  REFERENCES CHEF (CPF)
  
  CREATE SEQUENCE S_RECEITA 
	MINVALUE 1 MAXVALUE 999999 INCREMENT BY 1 START WITH 1 ;  
    
--------------------------------------------------------
--  DDL for Table INGREDIENTES
--------------------------------------------------------

 CREATE TABLE INGREDIENTE (
   CODIGO INT NOT NULL,
   DESCRICAO VARCHAR(150) NOT NULL,
   CODIGO_RECEITA INT NOT NULL
);
    
  ALTER TABLE INGREDIENTE 
  ADD FOREIGN KEY (CODIGO_RECEITA) 
  REFERENCES RECEITA (CODIGO)
  
